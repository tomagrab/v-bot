<div class="info-box w-2/5 border-4 rounded p-4 mt-4">
  <h2 class="info-box__subtitle">Quickstart:</h2>
  <ul class="info-box__text">
    <li>
      1. Navigate to <a
        class="info-box__text__link"
        href="https://vtrack.velsol.com/"
        target="_blank">V-Track</a
      > web and login
    </li>
    <li>2. Click on the chat icon on the bottom right corner</li>
    <li>3. Confirm or deny the first name that V-bot presents to you</li>
    <li class="pl-4">3.1 If the name is correct, reply "Yes"</li>
    <li class="pl-4">
      3.2 If the name is incorrect, reply "No" and enter "My name is [your first
      name]" in the chat window
    </li>
    <li>4. V-bot will present you with a list of topics it can assist with</li>
    <li>
      5. Reply with a question about one of these topics, such as "How do I add
      a user?"
    </li>
    <li>
      6. If V-bot does not understand or cannot answer your question, it will
      ask you to rephrase the question
    </li>
    <li>
      7. If V-bot still cannot answer your question, you can connect to a live
      agent by repying "Agent"
    </li>
  </ul>
</div>

